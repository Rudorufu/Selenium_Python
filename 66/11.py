from selenium import webdriver
url = 'https://parsinger.ru/methods/3/index.html'


with webdriver.Chrome() as browser:
    browser.get(url)
    cook = browser.get_cookies()
    sum_of_values = 0
    for i in cook:
        sum_of_values += int(i['value'])
    print(sum_of_values)