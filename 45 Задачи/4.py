# –ó–∞–¥–∞—á–∏
#
#     –í—Ö–æ–¥ –≤ –¶–∏—Ñ—Ä–æ–≤–æ–π –õ–∞–±–∏—Ä–∏–Ω—Ç: –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ Selenium –¥–ª—è –æ—Ç–∫—Ä—ã—Ç–∏—è —É–∫–∞–∑–∞–Ω–Ω–æ–≥–æ –≤–µ–±-—Å–∞–π—Ç–∞.
#     –ò–∑–≤–ª–µ—á–µ–Ω–∏–µ –§—Ä–∞–≥–º–µ–Ω—Ç–æ–≤: –ù–∞–π–¥–∏—Ç–µ –∏ –∏–∑–≤–ª–µ–∫–∏—Ç–µ –¥–∞–Ω–Ω—ã–µ –∏–∑ –∫–∞–∂–¥–æ–≥–æ –≤—Ç–æ—Ä–æ–≥–æ —Ç–µ–≥–∞ <p> –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ.
#     –í–æ—Å—Å–æ–∑–¥–∞–Ω–∏–µ –ê—Ä—Ç–µ—Ñ–∞–∫—Ç–∞: –ü—Ä–æ—Å—É–º–º–∏—Ä—É–π—Ç–µ –≤—Å–µ —á–∏—Å–ª–æ–≤—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è, –ø–æ–ª—É—á–µ–Ω–Ω—ã–µ –∏–∑ —ç—Ç–∏—Ö —Ç–µ–≥–æ–≤.
#     –ö–ª—é—á –∫ –ó–∞–≥–∞–¥–∫–µ: –ó–∞–ø–∏—à–∏—Ç–µ –ø–æ–ª—É—á–µ–Ω–Ω—É—é —Å—É–º–º—É –≤ –ø—Ä–µ–¥–Ω–∞–∑–Ω–∞—á–µ–Ω–Ω–æ–µ –¥–ª—è —ç—Ç–æ–≥–æ –ø–æ–ª–µ –∏–ª–∏ –≤—ã–≤–µ–¥–∏—Ç–µ –Ω–∞ —ç–∫—Ä–∞–Ω.


from selenium import webdriver
from selenium.webdriver.common.by import By


with webdriver.Chrome() as browser:
    browser.get('http://parsinger.ru/selenium/3/3.html') # –í—Ö–æ–¥ –≤ –¶–∏—Ñ—Ä–æ–≤–æ–π –õ–∞–±–∏—Ä–∏–Ω—Ç: –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ Selenium –¥–ª—è –æ—Ç–∫—Ä—ã—Ç–∏—è —É–∫–∞–∑–∞–Ω–Ω–æ–≥–æ –≤–µ–±-—Å–∞–π—Ç–∞.
    links = browser.find_elements(By.XPATH, '//p[2]')  # –ò–∑–≤–ª–µ–∫–∏—Ç–µ –¥–∞–Ω–Ω—ã–µ –∏–∑ –∫–∞–∂–¥–æ–≥–æ —Ç–µ–≥–∞ <p> –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ.
    sum_of_links = 0
    for i in links: # –ù–∞–π–¥–∏—Ç–µ –∏ –∏–∑–≤–ª–µ–∫–∏—Ç–µ –¥–∞–Ω–Ω—ã–µ –∏–∑ –∫–∞–∂–¥–æ–≥–æ –≤—Ç–æ—Ä–æ–≥–æ —Ç–µ–≥–∞ <p> –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ
        sum_of_links += int(i.text)  # –ü—Ä–æ—Å—É–º–º–∏—Ä—É–π—Ç–µ –≤—Å–µ —á–∏—Å–ª–æ–≤—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è, –ø–æ–ª—É—á–µ–Ω–Ω—ã–µ –∏–∑ —ç—Ç–∏—Ö —Ç–µ–≥–æ–≤.
    print(sum_of_links) # –ó–∞–ø–∏—à–∏—Ç–µ –ø–æ–ª—É—á–µ–Ω–Ω—É—é —Å—É–º–º—É –≤ –ø—Ä–µ–¥–Ω–∞–∑–Ω–∞—á–µ–Ω–Ω–æ–µ –¥–ª—è —ç—Ç–æ–≥–æ –ø–æ–ª–µ –∏–ª–∏ –≤—ã–≤–µ–¥–∏—Ç–µ –Ω–∞ —ç–∫—Ä–∞–Ω.