# –ü–µ—Ä–µ–¥ –≤–∞–º–∏ –ª–µ–∂–∏—Ç –º–∏—Å—Å–∏—è, –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–æ–≤–µ—Ä–∏—Ç –≤–∞—à–µ —É–º–µ–Ω–∏–µ –≤–Ω–∏–º–∞—Ç–µ–ª—å–Ω–æ "—á–∏—Ç–∞—Ç—å" –≤–µ–±-—Å—Ç—Ä–∞–Ω–∏—Ü—ã –∏ –∏–∑–≤–ª–µ–∫–∞—Ç—å –∏–∑ –Ω–∏—Ö –Ω—É–∂–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é.
# –ù–∞ —Ç–∞–∏–Ω—Å—Ç–≤–µ–Ω–Ω–æ–π —Å—Ç—Ä–∞–Ω–∏—Ü–µ —Å–∫—Ä—ã—Ç—ã —Ñ—Ä–∞–≥–º–µ–Ω—Ç—ã –∞—Ä—Ç–µ—Ñ–∞–∫—Ç–∞ ‚Äî –≤—Å–µ–≥–æ –∏—Ö 100.
# –û–Ω–∏ –∑–∞—à–∏—Ñ—Ä–æ–≤–∞–Ω—ã –∏ —Ä–∞–∑–º–µ—â–µ–Ω—ã –≤–æ –≤—Ç–æ—Ä—ã—Ö –∞–±–∑–∞—Ü–∞—Ö –∫–∞–∂–¥–æ–≥–æ –∏–∑ 200 –±–ª–æ–∫–æ–≤ —Ç–µ–∫—Å—Ç–∞.
# –í–∞—à–∞ –∑–∞–¥–∞—á–∞ ‚Äî —Å–æ–±—Ä–∞—Ç—å –∏—Ö –∏ –≤–æ—Å—Å–æ–∑–¥–∞—Ç—å –∞—Ä—Ç–µ—Ñ–∞–∫—Ç.
