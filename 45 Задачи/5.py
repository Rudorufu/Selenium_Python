#     –û—Ç–∫—Ä—ã–≤–∞–Ω–∏–µ –°–µ–∫—Ä–µ—Ç–∞: –ö–∞–∫ —Ç–æ–ª—å–∫–æ –≤—Å–µ —á–µ–∫-–±–æ–∫—Å—ã –±—É–¥—É—Ç –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω—ã, –Ω–∞–∂–º–∏—Ç–µ –Ω–∞ –∫–Ω–æ–ø–∫—É.
#     –î–æ—Å—Ç—É–ø –∫ –°–µ–∫—Ä–µ—Ç–Ω—ã–º –î–∞–Ω–Ω—ã–º: –°–∫–æ–ø–∏—Ä—É–π—Ç–µ —á–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ –ø–æ—è–≤–∏—Ç—Å—è –≤ —Ç–µ–≥–µ <p id="result">Result</p>.


from selenium import webdriver
from selenium.webdriver.common.by import By


with webdriver.Chrome() as browser:
    browser.get('http://parsinger.ru/selenium/4/4.html') # –û—Ç–∫—Ä–æ–π—Ç–µ –≤–µ–±-—Å–∞–π—Ç —Å –ø–æ–º–æ—â—å—é Selenium.
    boxes = browser.find_elements(By.CLASS_NAME, 'check') # –ù–∞–π–¥–∏—Ç–µ –≤—Å–µ —á–µ–∫-–±–æ–∫—Å—ã –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ
    for i in boxes:
        i.click() # —É—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ –∏—Ö –≤ –ø–æ–ª–æ–∂–µ–Ω–∏–µ checked —Å –ø–æ–º–æ—â—å—é .click().
    browser.find_element(By.CLASS_NAME, 'btn').click() # –ö–∞–∫ —Ç–æ–ª—å–∫–æ –≤—Å–µ —á–µ–∫-–±–æ–∫—Å—ã –±—É–¥—É—Ç –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω—ã, –Ω–∞–∂–º–∏—Ç–µ –Ω–∞ –∫–Ω–æ–ø–∫—É.
    result = browser.find_element(By.ID, 'result').text # –°–∫–æ–ø–∏—Ä—É–π—Ç–µ —á–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ –ø–æ—è–≤–∏—Ç—Å—è –≤ —Ç–µ–≥–µ <p id="result">Result</p>.
    print(result)
