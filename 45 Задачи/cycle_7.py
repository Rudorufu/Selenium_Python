#     –ü–æ–ª—É—á–µ–Ω–∏–µ –ö–æ–Ω–µ—á–Ω–æ–≥–æ –†–µ–∑—É–ª—å—Ç–∞—Ç–∞: –ö–æ–ø–∏—Ä—É–π—Ç–µ –¥–ª–∏–Ω–Ω–æ–µ —á–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ –ø–æ—è–≤–∏—Ç—Å—è –ø–æ—Å–ª–µ –Ω–∞–∂–∞—Ç–∏—è –Ω–∞ –∫–Ω–æ–ø–∫—É.
