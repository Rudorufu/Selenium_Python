from selenium import webdriver
from selenium.webdriver.common.by import By

with webdriver.Chrome() as browser:
    browser.get('http://parsinger.ru/selenium/7/7.html') # –û—Ç–∫—Ä–æ–π—Ç–µ —Å–∞–π—Ç —Å –ø–æ–º–æ—â—å—é Selenium
    list_of_options = browser.find_elements(By.TAG_NAME, 'option') # –ü–æ–ª—É—á–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏—è –≤—Å–µ—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤—ã–ø–∞–¥–∞—é—â–µ–≥–æ —Å–ø–∏—Å–∫–∞.
    sums = 0
    for i in list_of_options:
        sums += int(i.text) # –°–ª–æ–∂–∏—Ç–µ (–ø–ª—é—Å—É–π—Ç–µ) –≤—Å–µ –∏–∑–≤–ª–µ—á–µ–Ω–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è.
    browser.find_element(By.ID, 'input_result').send_keys(sums) # –í—Å—Ç–∞–≤—å—Ç–µ –ø–æ–ª—É—á–∏–≤—à–∏–π—Å—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤ –ø–æ–ª–µ –Ω–∞ —Å–∞–π—Ç–µ –∏
    browser.find_element(By.ID, 'sendbutton').click() # –Ω–∞–∂–º–∏—Ç–µ –∫–Ω–æ–ø–∫—É.
    print(browser.find_element(By.ID, 'result').text) # –ö–æ–ø–∏—Ä—É–π—Ç–µ –¥–ª–∏–Ω–Ω–æ–µ —á–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ –ø–æ—è–≤–∏—Ç—Å—è –ø–æ—Å–ª–µ –Ω–∞–∂–∞—Ç–∏—è –Ω–∞ –∫–Ω–æ–ø–∫—É

