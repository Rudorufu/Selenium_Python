#     –ö–ª—é—á–∏ –∫ –°–æ–∫—Ä–æ–≤–∏—â–∞–º: –ò–∑–≤–ª–µ–∫–∏—Ç–µ –¥–∞–Ω–Ω—ã–µ –∏–∑ –∫–∞–∂–¥–æ–≥–æ —Ç–µ–≥–∞ <p> –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ.
#     –°–ª–æ–∂–µ–Ω–∏–µ –§—Ä–∞–≥–º–µ–Ω—Ç–æ–≤: –ü—Ä–æ—Å—É–º–º–∏—Ä—É–π—Ç–µ –≤—Å–µ —á–∏—Å–ª–æ–≤—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è, –∫–æ—Ç–æ—Ä—ã–µ –≤—ã –∏–∑–≤–ª–µ–∫–ª–∏.
#     –û—Ç—á–µ—Ç –æ –°–æ–∫—Ä–æ–≤–∏—â–∞—Ö: –í—ã–≤–µ–¥–∏—Ç–µ —Å—É–º–º—É –Ω–∞ —ç–∫—Ä–∞–Ω –≤ print(), –ø–æ–ª—É—á–µ–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –≤—Å—Ç–∞–≤—å—Ç–µ –≤ –ø–æ–ª–µ –æ—Ç–≤–µ—Ç–∞ —Å—Ç–µ–ø–∏–∫


from selenium import webdriver
from selenium.webdriver.common.by import By
with webdriver.Chrome() as browser:
    browser.get('http://parsinger.ru/selenium/3/3.html') # –í—Ö–æ–¥ –≤ –õ–∞–±–∏—Ä–∏–Ω—Ç: –û—Ç–∫—Ä–æ–π—Ç–µ —É–∫–∞–∑–∞–Ω–Ω—ã–π –≤–µ–±-—Å–∞–π—Ç —Å –ø–æ–º–æ—â—å—é Selenium.
    links = browser.find_elements(By.TAG_NAME, 'p') # –ò–∑–≤–ª–µ–∫–∏—Ç–µ –¥–∞–Ω–Ω—ã–µ –∏–∑ –∫–∞–∂–¥–æ–≥–æ —Ç–µ–≥–∞ <p> –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ.
    sum_of_links = 0
    for i in links:
        sum_of_links += int(i.text) # –ü—Ä–æ—Å—É–º–º–∏—Ä—É–π—Ç–µ –≤—Å–µ —á–∏—Å–ª–æ–≤—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è, –∫–æ—Ç–æ—Ä—ã–µ –≤—ã –∏–∑–≤–ª–µ–∫–ª–∏.
    print(sum_of_links) # –í—ã–≤–µ–¥–∏—Ç–µ —Å—É–º–º—É –Ω–∞ —ç–∫—Ä–∞–Ω –≤ print(), –ø–æ–ª—É—á–µ–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –≤—Å—Ç–∞–≤—å—Ç–µ –≤ –ø–æ–ª–µ –æ—Ç–≤–µ—Ç–∞ —Å—Ç–µ–ø–∏–∫