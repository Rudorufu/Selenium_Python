# –î–ª—è —Ç–æ–≥–æ —á—Ç–æ–±—ã END —Å—Ä–∞–±–æ—Ç–∞–ª, —Å–Ω–∞—á–∞–ª–∞ –Ω—É–∂–Ω–æ –≤–∑—è—Ç—å –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä –≤ —Ñ–æ–∫—É—Å, —Ç–æ –µ—Å—Ç—å –∫–ª–∏–∫–Ω—É—Ç—å –ø–æ –Ω–µ–º—É:
